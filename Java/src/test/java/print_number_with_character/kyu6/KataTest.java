package print_number_with_character.kyu6;

import org.junit.Test;

import static org.junit.Assert.assertEquals;
public class KataTest {
	@Test
	public void basicTests() {
		doTest(99, '$',
				"$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$\n" +
						"$                                      $\n" +
						"$   $$$$   $$$$   $$$$   $$$$   $$$$   $\n" +
						"$  $$  $$ $$  $$ $$  $$ $$  $$ $$  $$  $\n" +
						"$  $$  $$ $$  $$ $$  $$ $$  $$ $$  $$  $\n" +
						"$  $$  $$ $$  $$ $$  $$  $$$$   $$$$   $\n" +
						"$  $$  $$ $$  $$ $$  $$   $$     $$    $\n" +
						"$   $$$$   $$$$   $$$$   $$     $$     $\n" +
						"$                                      $\n" +
						"$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$");

		doTest(12345, '*',
				"****************************************\n" +
						"*                                      *\n" +
						"*    **    ****   ****  **  ** ******  *\n" +
						"*   ***   **  ** **  ** **  ** **      *\n" +
						"*  * **      **     **  **  ** *****   *\n" +
						"*    **     **      **   *****     **  *\n" +
						"*    **    **    **  **     **     **  *\n" +
						"*  ****** ******  ****      ** *****   *\n" +
						"*                                      *\n" +
						"****************************************");

		doTest(67890, '@',
				"@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@\n" +
						"@                                      @\n" +
						"@     @@  @@@@@@  @@@@   @@@@   @@@@   @\n" +
						"@    @@   @@  @@ @@  @@ @@  @@ @@  @@  @\n" +
						"@   @@@@     @@   @@@@  @@  @@ @@  @@  @\n" +
						"@  @@  @@   @@    @@@@   @@@@  @@  @@  @\n" +
						"@  @@  @@  @@    @@  @@   @@   @@  @@  @\n" +
						"@   @@@@   @@     @@@@   @@     @@@@   @\n" +
						"@                                      @\n" +
						"@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");
	}
	private void doTest(int n, char c, String expected) {
		assertEquals(expected, Kata.printNumber(n, c));
	}
}